package mx.com.sportsworld.sw.pojo;


public class RoutinePojo extends MainPojo {

	String idRoutine;
	String status;
	String week_id;
	String day_id;
	

	public String getWeek_id() {
		return week_id;
	}

	public void setWeek_id(String week_id) {
		this.week_id = week_id;
	}

	public String getDay_id() {
		return day_id;
	}

	public void setDay_id(String day_id) {
		this.day_id = day_id;
	}

	public String getIdRoutine() {
		return idRoutine;
	}

	public void setIdRoutine(String idRoutine) {
		this.idRoutine = idRoutine;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
